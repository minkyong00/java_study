package pub2504.hello;

// 이클립스 필수 단축키
public class Shortcut {

	public static void main(String[] args) {

		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		System.out.println(4);
		System.out.println(5);

		// Ctrl + Alt + 위/아래 화살표 : 줄 복사
		// Ctrl + D : 줄 삭제
		// Ctrl + M : 화면 확대/축소
		// Ctrl + 스페이스 : 코드 어시스트(코드 도움말)
		// Shift + Alt + X > J : Java 실행
		// Ctrl + F11 : Java 실행
		// Ctrl + Shift + O : 자동 임포트
		// Ctrl + Shift + F : 코드 포맷팅
		// Ctrl + / : 주석 처리
		// Ctrl + F : 검색
		// Ctrl + H : 프로젝트 전체 검색
		// F11 : 디버깅 모드
		// Ctrl + 왼/오른쪽 화살표 : 워드 단위 이동
		// Ctrl + Shift + 왼/오른쪽 화살표 : 워드 단위 선택
	
	}

}
